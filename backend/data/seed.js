const seedDB = async () => {
  try {
    await mongoose.connect(process.env.MONGO_URI || 'mongodb://127.0.0.1:27017/medcare');
    console.log("Connected to MongoDB for seeding...");
    
    // Clear existing
    await Medicine.deleteMany({});
    console.log("Cleared old medicines.");
    
    // Seed
    await Medicine.insertMany(medicinesData);
    console.log("Successfully seeded 22 medicines across 8 categories!");
    
    mongoose.connection.close();
    console.log("Seed connection closed.");
    process.exit(0);
  } catch (error) {
    console.error("Seeding failed:", error);
    process.exit(1);
  }
};

seedDB();
